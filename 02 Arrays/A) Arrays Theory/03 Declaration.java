// Declaration
/*
 * Array Declaration in Java
 * Definition:
 * Array declaration means creating a reference variable that can store multiple
 * values of the same data type.
 * 
 * 1️⃣ Only Declaration (no memory allocated yet)
 * int[] arr;
 * or
 * int arr[];
 * Both are valid in Java.
 * Meaning:
 * A variable arr is declared that can reference an integer array, but memory is
 * not created yet.
 * 
 * 2️⃣ Declaration + Memory Allocation
 * int[] arr = new int[5];
 * Meaning:
 * Array type → int
 * Array size → 5
 * Memory is allocated for 5 integers
 * Indexes created:
 * arr[0]
 * arr[1]
 * arr[2]
 * arr[3]
 * arr[4]
 */