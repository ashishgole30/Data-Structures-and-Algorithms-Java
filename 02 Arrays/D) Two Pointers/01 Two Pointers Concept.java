/*
 * What is Two Pointers?
 * Instead of using one loop, we use two indices (pointers) to solve problems
 * efficiently.
 * Usually:
 * left = 0 (start)
 * right = n-1 (end)
 * And we move them based on conditions.
 */

/*
 * Why use it?
 * To avoid:
 * nested loops (O(n²))
 * And convert into:
 * single loop (O(n))
 */